@CrewBase
class ResearchCrew:
    """Research crew for e-commerce product fetching and reporting"""

   
    agents_config = "config/configagents.yaml"
    tasks_config = "config/configtasks.yaml"

   
    @agent
    def research_master_agent(self) -> Agent:
        return Agent(
            tools=[DummyJSONTool(), FakeStoreTool(), EscuelajsTool()],
            verbose=True
        )

    @agent
    def llm_agent(self) -> Agent:
        return Agent(
            llm=LLM(
                model="gpt-4o-mini",
                temperature=0.7,
                api_key=st.secrets["OPENAI_API_KEY"]
            ),
            verbose=True
        )

    
    @task
    def research_task(self) -> Task:
        return Task()  

    @task
    def analysis_task(self) -> Task:
        output_dir = os.path.join(os.getcwd(), "output")
        os.makedirs(output_dir, exist_ok=True)
        return Task(output_file=os.path.join(output_dir, 'report.md'))  

   
    @crew
    def crew(self) -> Crew:
        return Crew(
            agents=self.agents,   
            tasks=self.tasks,     
            process=Process.sequential,
            verbose=True,
        )



st.title("E-Commerce Product Research and Analysis")
st.subheader("Powered by CrewAI")
st.write("Enter a keyword to search for products:")

in_put = st.text_input("Search Keyword", key="search_keyword")

if in_put:
    try:
        ans = ResearchCrew().crew().kickoff(inputs={"query": in_put})
        st.write(ans)
    except Exception as e:
        st.error(f"Error running CrewAI: {e}")
else:
    st.write("Please enter a keyword to search.")


    
