# -*- coding: utf-8 -*-
import os
import requests
from crewai import Agent, Crew, Process, Task, LLM
from crewai.project import CrewBase, agent, crew, task
from crewai.tools import BaseTool
import streamlit as st


class DummyJSONTool(BaseTool):
    name: str = "DummyJSON API Tool"
    description: str = "Fetch products from DummyJSON using keyword search"

    def _run(self, query: str) -> str:
        try:
            url = f"https://dummyjson.com/products/search?q={query}"
            response = requests.get(url, timeout=10)
            response.raise_for_status()
            data = response.json()
            products = data.get("products", [])
            if not products:
                return "No products found for this keyword."
            return "\n".join([f"{p['title']} - ${p['price']} ({p['category']})" for p in products[:5]])
        except Exception as e:
            return f"Error: {str(e)}"


